int main(){
    int n;
    cin >> n;
    int ans = 1e6;
    for(int i = 1; i <= 1000; i++){
        for(int j = 1; j <= 1000; j++){
            if (i * j > n) continue;
            int sum = abs(i-j) + (n - i*j);
            ans = min(ans, sum);
        }
    }
    cout << ans << endl;
    return 0;
}